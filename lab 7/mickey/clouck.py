import pygame
import sys
from datetime import datetime

# Инициализация Pygame
pygame.init()

# Настройка окна
ширина, высота = 400, 200
экран = pygame.display.set_mode((ширина, высота))
pygame.display.set_caption("Цифровые часы")

# Цвета
БЕЛЫЙ = (255, 255, 255)
ЧЕРНЫЙ = (0, 0, 0)

# Шрифт
шрифт = pygame.font.Font(None, 48)  # Измените None на путь к файлу шрифта

def нарисовать_часы():
    # Получение текущего времени
    текущее_время = datetime.now().strftime("%H:%M:%S")

    # Отображение текста
    поверхность_текста = шрифт.render(текущее_время, True, БЕЛЫЙ)

    # Получение прямоугольника текста
    прямоугольник_текста = поверхность_текста.get_rect()

    # Центрирование текста
    прямоугольник_текста.center = (ширина // 2, высота // 2)

    # Заполнение экрана черным цветом
    экран.fill(ЧЕРНЫЙ)

    # Отображение текста на экране
    экран.blit(поверхность_текста, прямоугольник_текста)

# Основной цикл
while True:
    for событие in pygame.event.get():
        if событие.type == pygame.QUIT:
            pygame.quit()
            sys.exit()

    # Вызов функции нарисовать_часы
    нарисовать_часы()

    # Обновление экрана
    pygame.display.flip()

    # Ограничение кадров в секунду
    pygame.time.Clock().tick()
